package pjc21.mods.potionstacker.config;

public class EffectValues {
    private int ampMax;
    private int durMax;

    public int getAmpMax() {
        return ampMax;
    }
    public int getDurMax() {
        return durMax;
    }

    public void setAmpMax(int amplifier) { this.ampMax = amplifier; }

    public void setDurMax(int duration) {
        this.durMax = duration;
    }
}
